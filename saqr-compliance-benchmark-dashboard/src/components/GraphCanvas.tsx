import React from 'react';
import { motion } from 'motion/react';
import { ArrowRightLeft, ShieldAlert, ShieldCheck, Database, Calendar, Percent } from 'lucide-react';
import { RelationshipGraph } from '../types';

interface GraphCanvasProps {
  graphData: RelationshipGraph | null;
  isLoading?: boolean;
}

export const GraphCanvas: React.FC<GraphCanvasProps> = ({ graphData, isLoading }) => {
  if (isLoading) {
    return (
      <div className="flex h-64 flex-col items-center justify-center rounded border border-dashed border-slate-200 bg-slate-50/50 dark:border-white/5 dark:bg-[#0c0c0c]">
        <div className="h-8 w-8 animate-spin rounded-full border-2 border-[#c5a059] border-t-transparent" />
        <span className="mt-3 text-xs text-slate-500">Querying Neo4j GDS relationships...</span>
      </div>
    );
  }

  if (!graphData) {
    return (
      <div className="flex h-64 flex-col items-center justify-center rounded border border-dashed border-slate-200 bg-slate-50/50 p-6 text-center dark:border-white/5 dark:bg-[#0c0c0c]">
        <Database size={24} className="mb-2 text-slate-400" />
        <span className="text-xs font-semibold text-slate-600 dark:text-slate-400">No Graph Data Retrieved</span>
        <p className="mt-1 max-w-xs text-[11px] text-slate-500">
          Run a transaction to see relationships generated by Agent 2 (Graph Intelligence Engine).
        </p>
      </div>
    );
  }

  const { sender_id, receiver_id, metrics } = graphData;
  const isHighTrust = (metrics?.structural_trust_score || 0) >= 0.7;

  return (
    <div className="rounded border border-slate-150 bg-white p-5 dark:border-white/5 dark:bg-[#0c0c0c]">
      <div className="mb-4 flex items-center justify-between">
        <div>
          <h4 className="text-xs font-bold uppercase tracking-wider text-slate-400">GDS 2-Node Cluster</h4>
          <span className="text-xs text-slate-500">Interactive Neo4j Schema mapping</span>
        </div>
        <div className="flex items-center gap-1.5 rounded-full bg-slate-100 px-2.5 py-1 text-[10px] font-medium text-slate-600 dark:bg-[#050505] dark:border dark:border-white/5 dark:text-slate-400">
          <Database size={10} className="text-[#c5a059]" />
          <span>Neo4j Active Node Count: 2</span>
        </div>
      </div>

      <div className="relative flex h-48 w-full items-center justify-between px-8 md:px-16">
        {/* Background Connection Line */}
        <div className="absolute top-1/2 left-0 h-[2px] w-full -translate-y-1/2 bg-slate-100 dark:bg-[#1a1a1a]" />

        {/* Animated Flow Link */}
        <svg className="absolute inset-0 h-full w-full pointer-events-none">
          <defs>
            <linearGradient id="flowGrad" x1="0%" y1="0%" x2="100%" y2="0%">
              <stop offset="0%" stopColor="#c5a059" stopOpacity="0.1" />
              <stop offset="50%" stopColor="#c5a059" stopOpacity="1" />
              <stop offset="100%" stopColor="#c5a059" stopOpacity="0.1" />
            </linearGradient>
          </defs>
          <motion.line
            x1="15%"
            y1="50%"
            x2="85%"
            y2="50%"
            stroke="url(#flowGrad)"
            strokeWidth="3"
            strokeDasharray="10, 15"
            animate={{
              strokeDashoffset: [-50, 0]
            }}
            transition={{
              repeat: Infinity,
              duration: 2.5,
              ease: 'linear'
            }}
          />
        </svg>

        {/* Sender Node */}
        <motion.div
          initial={{ scale: 0.9, opacity: 0 }}
          animate={{ scale: 1, opacity: 1 }}
          className="relative z-10 flex h-16 w-16 flex-col items-center justify-center rounded-full border-2 border-[#c5a059] bg-white text-slate-900 shadow-md dark:border-[#c5a059] dark:bg-[#050505]"
        >
          <div className="text-[10px] font-bold text-[#c5a059]">SENDER</div>
          <span className="font-mono text-xs font-extrabold text-slate-700 dark:text-slate-300">
            #{sender_id}
          </span>
          <div className="absolute -bottom-6 left-1/2 -translate-x-1/2 whitespace-nowrap text-[9px] font-mono text-slate-400">
            Account Node
          </div>
        </motion.div>

        {/* Animated Connector Label */}
        <div className="relative z-10 flex flex-col items-center justify-center rounded border border-slate-200/60 bg-slate-50/95 px-3 py-1.5 shadow-sm dark:border-white/5 dark:bg-[#050505]">
          <ArrowRightLeft size={12} className="text-slate-400 animate-pulse" />
          <span className="mt-0.5 font-mono text-[10px] font-bold text-slate-700 dark:text-slate-300">
            {metrics?.average_amount ? `${metrics.average_amount.toLocaleString()} SAR` : '0 SAR'}
          </span>
          <span className="text-[8px] font-medium text-slate-400">Avg Tx Weight</span>
        </div>

        {/* Receiver Node */}
        <motion.div
          initial={{ scale: 0.9, opacity: 0 }}
          animate={{ scale: 1, opacity: 1 }}
          className="relative z-10 flex h-16 w-16 flex-col items-center justify-center rounded-full border-2 border-slate-300 bg-white text-slate-900 shadow-md dark:border-white/20 dark:bg-[#050505]"
        >
          <div className="text-[10px] font-bold text-slate-400">BENEFICIARY</div>
          <span className="font-mono text-xs font-extrabold text-slate-700 dark:text-slate-300">
            #{receiver_id}
          </span>
          <div className="absolute -bottom-6 left-1/2 -translate-x-1/2 whitespace-nowrap text-[9px] font-mono text-slate-400">
            Target Account
          </div>
        </motion.div>
      </div>

      {metrics && (
        <div className="mt-8 grid grid-cols-2 gap-3.5 border-t border-slate-150 pt-4 md:grid-cols-4 dark:border-white/5">
          <div className="rounded bg-slate-50/50 p-2.5 dark:bg-[#050505] dark:border dark:border-white/5">
            <div className="flex items-center gap-1 text-slate-500 dark:text-slate-400">
              <Percent size={11} />
              <span className="text-[10px] font-medium">Structural Trust</span>
            </div>
            <div className="mt-1 flex items-baseline gap-1">
              <span className="font-mono text-xs font-extrabold text-slate-800 dark:text-slate-200">
                {(metrics.structural_trust_score * 100).toFixed(0)}%
              </span>
              <span className="text-[8px] font-medium text-slate-400">GDS-derived</span>
            </div>
          </div>

          <div className="rounded bg-slate-50/50 p-2.5 dark:bg-[#050505] dark:border dark:border-white/5">
            <div className="flex items-center gap-1 text-slate-500 dark:text-slate-400">
              <Calendar size={11} />
              <span className="text-[10px] font-medium">Relationship Age</span>
            </div>
            <div className="mt-1 flex items-baseline gap-1">
              <span className="font-mono text-xs font-extrabold text-slate-800 dark:text-slate-200">
                {(metrics.relationship_age_seconds / 3600).toFixed(1)} hrs
              </span>
              <span className="text-[8px] font-medium text-slate-400">Historical link</span>
            </div>
          </div>

          <div className="rounded bg-slate-50/50 p-2.5 dark:bg-[#050505] dark:border dark:border-white/5">
            <div className="flex items-center gap-1 text-slate-500 dark:text-slate-400">
              <ArrowRightLeft size={11} />
              <span className="text-[10px] font-medium">Temporal Consistency</span>
            </div>
            <div className="mt-1 flex items-baseline gap-1">
              <span className="font-mono text-xs font-extrabold text-slate-800 dark:text-slate-200">
                {metrics.temporal_consistency.toFixed(2)}
              </span>
              <span className="text-[8px] font-medium text-slate-400">Z-time pattern</span>
            </div>
          </div>

          <div className="rounded bg-slate-50/50 p-2.5 dark:bg-[#050505] dark:border dark:border-white/5">
            <div className="flex items-center gap-1 text-slate-500 dark:text-slate-400">
              {isHighTrust ? (
                <ShieldCheck size={11} className="text-emerald-500" />
              ) : (
                <ShieldAlert size={11} className="text-amber-500" />
              )}
              <span className="text-[10px] font-medium">Regularity Index</span>
            </div>
            <div className="mt-1 flex items-baseline gap-1">
              <span className="font-mono text-xs font-extrabold text-slate-800 dark:text-slate-200">
                {metrics.transaction_regularity.toFixed(2)}
              </span>
              <span className="text-[8px] font-medium text-slate-400">Velocity-norm</span>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};
